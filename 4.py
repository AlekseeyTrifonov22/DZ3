x1 = int(input("Размер файла в гигобайтах:  "))
x2 = int(input("Скорость интернета в битах в секунду:  "))
f = int(input("1 - в секундах\n2 - в минутах\n3 - в часах\n"))
if f == 1:
    print(x1 * 1024 * 1024 * 1024 * 8 / x2)
elif f == 2:
    print(x1 * 1024 * 1024 * 1024 * 8 / 60 / x2)
elif f == 3:
    print(x1 * 1024 * 1024 * 1024 * 8 / 3600 / x2)
